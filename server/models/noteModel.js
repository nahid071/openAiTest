const mongoose = require("mongoose")

const noteSchema = mongoose.Schema(
  {
    user: {
      type: mongoose.Schema.Types.ObjectId,
      required: true,
      ref: "User",
    },
    title: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
  },
  {
    timeStamps: true,
  }
)

module.exports = mongoose.model("Notes", noteSchema)
